from math import pi


class Circle:
    def get_name (self):
        for i, j in globals().items():
            if j is self:
                return i

    def __init__ (self, radius):
        self.radius = radius

    def __str__ (self):
        return (f'–û–∫—Ä—É–∂–Ω–æ—Å—Ç—å {self.get_name()} —Ä–∞–¥–∏—É—Å–æ–º {self.radius} –∏–º–µ–µ—Ç —Å–ª–µ–¥—É—é—â–∏–µ –ø–∞—Ä–∞–º–µ—Ç—Ä—ã:\n'
                f'–î–ª–∏–Ω–∞ –æ–∫—Ä—É–∂–Ω–æ—Å—Ç–∏: {round(2 * pi * self.radius, 2)}\n'
                f'–ü–ª–æ—â–∞–¥—å –æ–∫—Ä—É–∂–Ω–æ—Å—Ç–∏: {round(pi * self.radius ** 2, 2)}\n')


pupa = Circle(30)
lupa = Circle(20)
print(pupa)
